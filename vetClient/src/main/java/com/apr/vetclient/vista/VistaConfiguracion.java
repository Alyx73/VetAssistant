    private void cargarValoresAlmacenados() {
        try {
            config = new PreferenciasConfig();
            txtHost.setText(config.getHost());
            txtPuerto.setText(config.getPuerto());
            txtUser.setText(config.getUsuario());
            txtPasswd.setText(config.getContrasena());
            txtPasswordVF.setText(config.getContrasena());
        } catch (IOException ex) {  // Esta excepcion nunca saltara. Si no encuentra el archivo lo crea con los valores por defecto. 
                                    //Pongo el mensaje por si existe un caso que no contemplo...  
            JOptionPane.showMessageDialog(rootPane, "No se puedieron cargar los valores almacenados.", "Error al acceder al archivo de configuración.", ERROR);
        }
    }

    private boolean camposVacios() {
        return txtHost.getText().isBlank() || txtPuerto.getText().isBlank() || txtUser.getText().isBlank() 
                || new String(txtPasswd.getPassword()).isBlank() || new String(txtPasswordVF.getPassword()).isBlank() ;
    }

